                <p>Java programming is known as Object Oriented Programming (OOP). To understand Objects, think about a person. A person has a first name, last name, phone number, email, etc. Just as you would save all of this information, Java saves this information in a similar way. Each object belongs to a Class- the template in which function are programmed on top of and then referenced when needed. Saving all of the information in Objects allows for classes to easily communicate with each other to share information. Think of this as two people talking, they trade information by asking questions and receiving answers. The goal of Java programmers is to think of the questions that are going to be asked and to let the Objects to respond with the answers that we need.</p>
